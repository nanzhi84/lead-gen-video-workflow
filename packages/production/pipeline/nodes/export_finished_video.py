COVER_PROMPT_VERSION_ID = "prompt_cover_ai_cover_v1"
# Spec §10.1: the AI-cover prompt must resolve through the Prompt Registry binding
# (PublishCover.ai_cover), not be looked up by a hardcoded version id. We still
